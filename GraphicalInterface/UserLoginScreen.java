    void display()
    {
        ArrayList<UserLoginFormBean> list = new UserLoginFormAction().getAllRecords();
        String columns[]={"username","email","contact number","gender","roll type"};
        if(list!=null)
        {
            Object data[][] = new Object[list.size()][columns.length];
            int idx = 0;
            for(UserLoginFormBean s : list)
            {
                data[idx]= new Object[]{s.getUserName(),s.getEmail(),s.getContactName(),s.getGender(),s.getRoletype()};
                idx++;
                
                DefaultTableModel dt = new DefaultTableModel(data,columns)
                {
                    @Override
                    public boolean isCellEditable(int rows,int columns)
                    {
                        return false;
                    }
                };
                tableRecord.setModel(dt);             
                
            }
        }
    }
    
